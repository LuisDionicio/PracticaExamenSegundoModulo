package PreguntaUno;

public class Cliente {
private int codiClie;
private String nombClie;

    public Cliente(int codiClie, String nombClie) {
        this.codiClie = codiClie;
        this.nombClie = nombClie;
    }

    public int getCodiClie() {
        return codiClie;
    }

    public String getNombClie() {
        return nombClie;
    }




}